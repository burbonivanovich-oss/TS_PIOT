// продуктов по строкам. Значения правдивы по сути продукта; где нет
// конкретной цифры — «по тарифу», «да», «нет», «—».
//
// products[] — список bannerId; колонки таблицы. values[i] строки
// соответствует products[i]. Каждый bannerId — ключ в CPA_BANNERS и slug
// страницы /produkty/<bannerId>/.

export interface ComparisonRow {
	label: string;
	values: string[];
}

export interface ComparisonTable {
	/** slug страницы: /sravneniya/<slug>/ */
	slug: string;
	/** заголовок таблицы и страницы */
	title: string;
	/** надзаголовок над h1 */
	eyebrow: string;
	/** лид под заголовком */
	lead: string;
	/** bannerId-колонки — ключи CPA_BANNERS и slug /produkty/ */
	products: string[];
	/** строки сравнения; values[i] ↔ products[i] */
	rows: ComparisonRow[];
}

