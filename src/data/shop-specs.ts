// Подробные описания и характеристики товаров магазина.
// Данных по «железным» ТТХ (экран, процессор, аккумулятор) в фиде нет,
// поэтому здесь — честная информация: тип устройства (из названия),
// соответствие 54-ФЗ, работа с маркировкой/ЕГАИС/ОФД через Контур,
// условия (гарантия, регистрация). Точные ТТХ менеджер уточняет.
import type { ShopItem, ShopSection } from './shop-catalog';

const months = (name: string) => Number(name.match(/(\d+)\s*мес/)?.[1] ?? 0);

/** Тип кассового устройства из названия (без домыслов по модели). */
export function productType(name: string): string {
	const n = name.toLowerCase();
	if (n.includes('смарт')) return 'Смарт-терминал';
	if (n.includes('pos-терминал') || n.includes('pos терминал')) return 'POS-терминал';
	if (n.includes('мобильн')) return 'Мобильная касса';
	if (n.includes('касса')) return 'Онлайн-касса';
	return 'Контрольно-кассовая техника';
}

/** Подробное описание товара (массив абзацев). */
export function longDescription(item: ShopItem, section: ShopSection): string[] {
	const p: string[] = [];
	if (item.desc) p.push(item.desc);

	if (section.id === 'kasses') {
		const type = productType(item.name);
		p.push(
			`${type} «${item.name}» — контрольно-кассовая техника по 54-ФЗ. Формирует и печатает кассовые чеки, а данные о каждой продаже передаёт в ФНС через оператора фискальных данных (ОФД).`
		);
		p.push(
			'Вместе с Контур.Маркет касса работает с обязательной маркировкой «Честный знак»: проверяет коды при продаже и передаёт сведения о выводе товара из оборота. Поддерживает продажу алкоголя через ЕГАИС и приём оплаты картами и по СБП через эквайринг.'
		);
		p.push(
			'Для запуска нужны фискальный накопитель и договор с ОФД — соберите комплект «касса под ключ» в блоке слева. Контур поможет с настройкой и зарегистрирует кассу в налоговой онлайн за 1 день.'
		);
		p.push(
			'Базовая комплектация: касса, кабель питания, документация и гарантийный талон. Точный состав и технические характеристики менеджер подтвердит при оформлении заявки.'
		);
	} else if (section.id === 'fn-ofd') {
		p.push(
			`Фискальный накопитель — обязательный модуль онлайн-кассы. Он шифрует и хранит данные о пробитых чеках и передаёт их оператору фискальных данных. Срок действия — ${months(item.name)} месяцев; по окончании ФН подлежит замене.`
		);
		p.push(
			'Выбирайте срок ФН под свой налоговый режим: для УСН и услуг обычно берут 36 месяцев, для ОСНО и торговли подакцизными товарами — 15 месяцев. Менеджер подскажет подходящий вариант.'
		);
	} else if (section.id === 'ofd') {
		p.push(
			'Оператор фискальных данных (ОФД) принимает чеки от кассы и в реальном времени передаёт их в ФНС, как требует 54-ФЗ. В личном кабинете доступны аналитика продаж и хранение чеков.'
		);
		p.push('Контур.ОФД совместим со всеми моделями касс и поддерживает работу с маркировкой «Честный знак».');
	} else if (section.id === 'edo') {
		p.push(
			'Контур.Диадок — юридически значимый электронный документооборот. Через него принимают и отправляют УПД с кодами маркировки, счета-фактуры и акты без бумаги, с электронной подписью.'
		);
	} else if (section.id === 'periphery') {
		p.push(
			'Периферийное оборудование для кассовой зоны. Подключается к онлайн-кассе и расширяет её возможности — от приёмки товара по штрихкодам до хранения наличных.'
		);
	}
	return p;
}

/** Таблица характеристик (только достоверные строки). */
export function characteristics(item: ShopItem, section: ShopSection): [string, string][] {
	const rows: [string, string][] = [['Раздел', section.title]];
	if (item.cat) rows.push(['Категория', item.cat]);
	rows.push(['Артикул', item.id]);

	if (section.id === 'kasses') {
		rows.push(
			['Тип устройства', productType(item.name)],
			['Соответствие 54-ФЗ', 'да'],
			['Передача данных в ОФД', 'да'],
			['Маркировка «Честный знак»', 'да, через Контур.Маркет'],
			['ЕГАИС (алкоголь)', 'да, через Контур.Маркет'],
			['Фискальный накопитель', 'приобретается отдельно'],
			['Регистрация в ФНС', 'онлайн за 1 день']
		);
	} else if (section.id === 'fn-ofd') {
		rows.push(
			['Тип', 'Фискальный накопитель (ФН)'],
			['Срок действия', `${months(item.name)} мес`],
			['Назначение', 'хранение и передача фискальных данных'],
			['Соответствие 54-ФЗ', 'да']
		);
	} else if (section.id === 'ofd') {
		rows.push(
			['Тип', 'Услуга ОФД'],
			['Срок', `${months(item.name)} мес`],
			['Передача чеков в ФНС', 'в реальном времени'],
			['Совместимость', 'все модели касс']
		);
	} else if (section.id === 'periphery') {
		rows.push(['Тип', 'Периферийное оборудование']);
	}

	rows.push(['Гарантия', '12 месяцев от производителя'], ['Наличие', 'на складе']);
	return rows;
}
